SSEG SEGMENT STACK
    DB 32 DUP(0)
SSEG ENDS

DSEG SEGMENT
    SCORE DB 56
    LEVEL DB 0
DSEG ENDS

CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG,SS:SSEG
START:
    MOV AX,DSEG
    MOV DS,AX
    XOR AX,AX
    
    MOV AL,SCORE
    CMP AL,90
    JB C80
    MOV AH,'A'
    JMP OK
C80:
    CMP AL,80
    JB C70
    MOV AH,'B'
    JMP OK
C70:
    CMP AL,70
    JB C60
    MOV AH,'C'
    JMP OK
C60:
    CMP AL,60
    JB L_E
    MOV AH,'D'
    JMP OK
L_E:
    MOV AH,'E'
OK:
    MOV LEVEL,AH
    MOV DL,AH
    MOV AH,02H
    INT 21H
    MOV AH,4CH
    INT 21H
CSEG ENDS
END START