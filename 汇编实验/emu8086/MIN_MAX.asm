SSEG SEGMENT STACK
    DB 32 DUP(0)
SSEG ENDS

DSEG SEGMENT
    TEST_NUM DB 3,-2,38,-25,0E9H,29,88,0,-69,77
    NUM = $-TEST_NUM
    MAXV DB 0
    MINV DB 0
DSEG ENDS

CSEG SEGMENT
ASSUME CS:CSEG,DS:DSEG,SS:SSEG
START:
    MOV AX,DSEG
    MOV DS,AX
    XOR AX,AX
    
    LEA SI,TEST_NUM
    MOV AL,[SI]
    MOV BL,[SI]
    MOV CX,NUM
    
GET:
    CMP AL,[SI]
    JG MAX 
    MOV AL,[SI]
MAX:
    CMP BL,[SI]
    JL MIN
    MOV BL,[SI]
MIN:
    INC SI
    LOOP GET
    
    MOV MAXV,AL
    MOV MINV,BL
            
    MOV AH,4CH
    INT 21H
CSEG ENDS
END START